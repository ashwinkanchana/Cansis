package com.ashwinkanchana.cansis.data;

public class Marks {
        private String name;
        private String value;


        public Marks() {
        }


        public Marks(String name,String value) {
            this.name = name;
            this.value = value;
        }


        public String getName() {
            return name;
        }
        public String getValue() {
            return value;
        }



}